function spa_get_personality($score) {

    if ($score >= 85) return [
        'type' => '最強',
        'emoji' => '😎🔥',
        'message' => 'めっちゃ調子いいじゃん！このまま伸びるやつ😏'
    ];

    if ($score >= 70) return [
        'type' => '優秀',
        'emoji' => '😊✨',
        'message' => 'いい感じ！ちょっと手入れすればさらに伸びるよ👍'
    ];

    if ($score >= 50) return [
        'type' => '普通',
        'emoji' => '🙂',
        'message' => '悪くはないけど…ちょっと物足りないかも😏'
    ];

    if ($score >= 30) return [
        'type' => '不調',
        'emoji' => '😵',
        'message' => 'ちょっと疲れてるね…放置気味じゃない？'
    ];

    return [
        'type' => '危険',
        'emoji' => '💀',
        'message' => 'これ普通にヤバいかも。今すぐ手入れしよ🔥'
    ];
}

